package com.javaex.ex01;

public class Ex09 {
	
	public static void main(String[] args) {
		
		//관계연산자
		int a = 5;
		int b = 3;
		
		System.out.println(a<b);
		System.out.println(a>b);

		System.out.println(a<=b);
		System.out.println(a>=b);

		System.out.println(a==b);
		System.out.println(a!=b);
		
		boolean result = a==b;
		System.out.println("---------------------------");
		System.out.println(result);
		
	}

}
