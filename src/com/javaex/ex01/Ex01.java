package com.javaex.ex01;

public class Ex01 {
	
	public static void main(String[] args) {
		
		int myAge;                    //선언
		myAge = 25;                   //초기화(초기값)
		
		System.out.println(myAge);    //화면출력
		
		myAge = 26;
		
		System.out.println(myAge);    //화면출력
		
		/********************************************/
		
		byte no;
		no = 127;
		
		System.out.println(no);
		
		/********************************************/
		
		long no2;                    //int를 넘을때만 L을 붙여준다
		no2 = 1239555877697L;
		
		System.out.println(no2);
	}

}
